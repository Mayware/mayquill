/*
 *  Types:
 *  int / uint : 32 bits
 *  fixed point: 24 bits whole, 8 bits decimal
 *  object     : 32 bits
 *  new_id     : 32 bits
 *  string     : 32 bits integer length prefix, contents (n bits, padded to the nearest 32 bits), \0 terminator
 *  array      : 32 bits integer length prefix, contents (n bits, padded to the nearest 32 bits
 *  fd         : 0 bits, stored in ancillary data of the message
 *  enum       : 32 bits integer
 */
std::unordered_map<std::string, std::string> types = {
	{"int", "int32_t"},
	{"uint", "uint32_t"},
	{"fixed", "int32_t"},
	{"object", "uint32_t"},
	{"new_id", "uint32_t"},
	{"string", "std::string"},
	{"array", "void*"},
	{"fd", "int"},
	{"enum", "uint32_t"},
};

struct Description {
    std::string summary;
    std::string full;
};

struct Argument {
    std::string name;
    std::string type;
    std::optional<std::string> interface_name;
    std::optional<std::string> enum_name;
    std::optional<std::string> allow_null;
    std::string summary;
};

struct Declaration {
    std::string name;
    std::string since;
    Description description;
    std::vector<Argument> arguments;
};


struct Enum {
    struct Entry {
        std::string name;
        std::string since;
        std::string value;
        std::string summary;
    };

    std::string name;
    Description description;
    std::vector<Entry> entries;
};

struct Interface {
    std::string name;
    Description description;
    std::string version;
    std::vector<Declaration> requests;
    std::vector<Declaration> events;
    std::vector<Enum> enums;
};

struct Protocol {
    std::string name;
    std::string copyright;
    std::vector<Interface> interfaces;
};

